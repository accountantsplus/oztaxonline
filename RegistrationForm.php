                            }
                    
                            this.updateClasses();
                    
                            if (this.elem) {
                    
                                this.setElementPosition();
                    
                            } else {
                    
                                this.setGlobalPosition();
                    
                            }
                    
                            this.show(true);
                    
                            if (this.options.autoHide) {
                    
                                clearTimeout(this.autohideTimer);
                    
