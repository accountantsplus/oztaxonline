            <h2>9.The ATO’s Substantiation Rules & Agreement </h2> 
            
             <img src="https://www.policetax.com.au/images/ATO111.jpg" alt="ATO Substaintiation Rules" style="width:60px;height:50px;">
            
            <p> </p> All Clients are reminded and agree that they can  substantiate all tax claims.
            The ATO has provided three rules in determining whether the deduction your claiming is eligible:
        <p> 1.	The taxpayer must have incurred the expense themselves – and not have been reimbursed  </p>  
           <p>2.	The expense must be incurred in gaining or producing assessable income.  </p>   
            <p>  3.	The claim must comply with the substantiation rules – i.e. all records must be kept. </p> 




            Records are usually a receipt from the supplier of the goods or services. You need to keep these for five years from
            when you lodge your tax return in case we ask you to substantiate your claims
            Your receipts must show:<br>
            
             <p> •	name of the supplier </p> 
             <p> •	amount of expense </p>  
             <p> •	nature of goods and services (noting the specific type of items purchased or expenditure incurred which I am able 
             to personally record up to the date of lodgement of my return where not adequately noted by the supplier)</p>  
             <p> •	date of expense (which I am able to personally record when not noted by the supplier) </p>  
             <p> •	date of the document.</p> </p>

            
            
            
            
            
